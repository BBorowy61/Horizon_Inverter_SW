/********************************************************************/
/********************************************************************/
/*	Divide function													*/
/*	returns 4096*x/y												*/
/********************************************************************/

int div_q12 (register int x,register int y)
{
	register int temp;
	if (y==0)
		return(32767);
	if ((temp=abs(y)) <= INVERSE_MIN)
	{
	while (temp <= INVERSE_MIN)
		{
			temp<<=1;
			if (abs(x) > 16383)
				return (32767);
			x<<=1;
		}
	}
	else
	{
		while (temp > 4095)
		{
			temp>>=1;
			x>>=1;
		}
	}
	temp=(long)x*inverse_table[temp-INVERSE_MIN]>>12;
	if (y > 0)					// restore sign
		return(temp);
	else
		return(-temp);
}

/********************************************************************/
/********************************************************************/
/*	Integer Square Root function									*/
/********************************************************************/

int int_sqrt (int input)
{
	register int temp,shift;
	if ((temp=input)<=0)
		return (0);
	shift=10;
	// normalize input to range 0 to 4096
	while (temp > (4096-1))
	{
		temp=temp>>2;
		shift--;
	}
	return((sqrt_table[temp])>>shift);
}

int long_sqrt (long input)
{
	register int shift;
	register long temp;
	temp=input;
	if ((temp>>16)<0)
		return (0);
	shift=10;
	// normalize input to range 0 to 4096
	while ((temp>>16)!=0)
	{
		temp=temp>>2;
		shift--;
	}
	while ((unsigned int)temp > (4096-1))
	{
		temp=temp>>2;
		shift--;
	}
	return((sqrt_table[(unsigned int)temp])>>shift);
}

/********************************************************************/
/*		Read NVRAM function											*/
/*		ALWAYS use this function to read NVRAM						*/
/********************************************************************/
void read_nvram(register unsigned far *src_pointer,register unsigned far *dest_pointer,register unsigned length)
{		
	if (nvram_busy==0)
	{
		nvram_busy=1;
		while (length!=0)
		{
			length--;
			*(dest_pointer++)=((*src_pointer++)&0xFF)|((*src_pointer++)<<8);
		}
		nvram_busy=0;
	}
}

/********************************************************************/
/*		Write NVRAM function										*/
/*		ALWAYS use this function to write NVRAM						*/
/********************************************************************/
void write_nvram(register unsigned far *src_pointer,register unsigned far *dest_pointer,register unsigned length)
{		
	if (nvram_busy==0)
	{
		nvram_busy=1;
		while (length!=0)
		{
			length--;
			*(dest_pointer++)=*src_pointer;
			*(dest_pointer++)=*(src_pointer++)>>8; 
		}
		nvram_busy=0;
	}
}


